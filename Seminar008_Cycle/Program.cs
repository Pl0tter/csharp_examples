// Условные и счётные циклы
int age = 10;
//string gender = "male";

// Условный цикл
while (age < 15)
{
    Console.WriteLine("Happy birthday, dude! You are " + age + " years old!");
    age++;
}

// Счетный цикл
for (int i = 1; i < 5; i++)
{
    Console.WriteLine("It is your " + i + " birthday!");
}